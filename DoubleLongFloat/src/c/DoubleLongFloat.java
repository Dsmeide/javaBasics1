package c;

public class DoubleLongFloat {

	public static void main(String[] args) {
		
		//https://cs.fit.edu/~ryan/java/language/java-data.html
		// more info
		// long integer, DONT FORGET THE "L" at the end.
		long a = 9999999999999999L;
		// float integer with decimal point , DONT FORGET THE F.
		float b = 2.5F;
		// double is a long integer,
		double c = 9999999999999999999999.999999999999999999999999;
		
		System.out.println(c);

	}

}
